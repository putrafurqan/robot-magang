#ifndef MAPPINGJOYSTICK_H
#define MAPPINGJOYSTICK_H

/*START and SELECT*/
#define START_val 0x60
#define SELECT_val 0x5F

/*NULL*/
#define NULL_val 0x00

/*Button*/
#define KOTAK 0x64
#define LINGKARAN 0x65
#define SEGITIGA 0x66
#define SILANG 0x67

/*Analog*/
#define RY_UP 0x68
#define RX_RIGHT 0x69
#define RY_DOWN 0x6A
#define RX_LEFT 0x6B
#define LY_UP 0x6C
#define LX_RIGHT 0x6D
#define LY_DOWN 0x6E
#define LX_LEFT 0x6F
#define L3_val 0x6C
#define R3_val 0x6D

/*D-Pad*/
#define UP 0x6E
#define DOWN 0x6F
#define LEFT 0x70
#define RIGHT 0x71

/*Shoulder*/
#define L1_val 0x72
#define R1_val 0x73
#define L2_val 0x74
#define R2_val 0x75

#endif